def write_inspection_page():
    bytes = resp.content
    with open('inspection_page.html', 'w') as outfile:
        outfile.write(bytes)
    return outfile


def load_inspection_page():
    content = open('inspection_page.html', 'r')
    resp_content = content.read()
    content.closed
    encoding = open('inspection_page_encoding.html', 'r')
    resp_encoding = encoding.read()
    encoding.closed
    return resp_content, resp_encoding


def get_inspection_page(**kwargs):
    url = INSPECTION_DOMAIN + INSPECTION_PATH
    params = INSPECTION_PARAMS.copy()
    for key, val in kwargs.items():
        if key in INSPECTION_PARAMS:
            params[key] = val
    resp = requests.get(url, params=params)
    resp.raise_for_status()
    return resp.content, resp.encoding


def parse_source(html, encoding='utf-8'):
    parsed = BeautifulSoup(html, 'html5lib', from_encoding=encoding)
    return parsed


def extract_data_listings(html):
    id_finder = re.compile(r'PR[\d]+~')
    return html.find_all('div', id=id_finder)


def has_two_tds(elem):
    is_tr = elem.name == 'tr'
    td_children = elem.find_all('td', recursive=False)
    has_two = len(td_children) == 2
    return is_tr and has_two


